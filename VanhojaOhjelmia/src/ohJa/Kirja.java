package ohJa;

public class Kirja implements Talletettava {
  
    private double paino;
    private String tekija;
    private String nimi;
    
    public Kirja(String tekija, String nimi, double paino) {
        this.paino = paino;
        this.tekija = tekija;
        this.nimi = nimi;
       
    }
    
    public double getPaino() {
        return paino;
    }
    
    public String toString() {
        return tekija + ": " + nimi;
    }
}